
public class Photo {
	
	protected int pixels;
	protected String type;

	
	public Photo() {
		this.pixels = 0;
		this.type = " ";
	}

	public int getPixels() {
		return pixels;
	}

	public void setPixels(int pixels) {
		this.pixels = pixels;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
	
}
